# Immutable data types
myTuple=(1,2,4,5,3)#tuple with multi elements
print(myTuple)
print(type(myTuple))

myTuple2=()#empty tuple
print(myTuple2)
print(type(myTuple2))

myTuple3=(1)#This takes as a int instead of tuple
myTuple4=(1,)#This is a single element tuple
print(myTuple3)
print(type(myTuple3))
print(myTuple4)
print(type(myTuple4))