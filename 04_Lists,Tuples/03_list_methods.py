numList=[10,3,5,8,9,4]

### Sorting ###
numList.sort()
print(numList)

### Reverse ###
numList.reverse()
print(numList)

### Append ###
numList.append(1)
print(numList)

### pop ###
numList.pop()#remove last item in a list
print(numList)
numList.pop(0)#remove the item in 0th index
print(numList)

### Remove ###
numList.remove(9)#remove the value 10 in the list
# remove method removes the first occurence if the has duplicate values
print(numList)